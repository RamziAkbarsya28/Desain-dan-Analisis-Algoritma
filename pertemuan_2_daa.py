def getSum(myList):
    total = 0
    for row in myList:
        for item in row:
            total += item
    return total

def getBagi2(setA, setB):
    sumA = getSum([setA])
    sumB = getSum([setB])
    if sumB == 0:   # supaya tidak error bagi 0
        return None
    return sumA / sumB

def getKurang2(setA, setB):
    sumA = getSum([setA])
    sumB = getSum([setB])
    return sumA - sumB

